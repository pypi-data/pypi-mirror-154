class CSSPropertyShorthand(ABC):
    short_name:str
    # ------------------------------------------------------------------------------------------------------------------
    # - generator -
    # ------------------------------------------------------------------------------------------------------------------
    def name_printer(self) -> str:
        return f"{self.short_name}"

    @abstractmethod
    def value_printer(self) -> str:...

    def printer(self) -> str:
        return f"{self.name_printer()}: {self.value_printer()}"

    def __str__(self) -> str:
        return self.printer()